"""Location Scout — picks a new, never-repeated location for each IA
run and tracks every run in an Excel workbook.

Flow (per the user's spec):
  1. Before picking anything, read the Excel tracker and build the set of
     city/state/country combos already used.
  2. Use web search + an LLM call to find a *new* polluted location (river,
     drain, slum, landfill, or polluted lake) anywhere in the target region,
     structured into the same JSON shape the user described:
       {location, country, pollution_type, viral_angle, source_image_query}
     plus the extra fields (area_name, city, state, type) the existing
     frame/clip prompt builders need.
  3. Append a new row to the tracker immediately (status "started") so a
     concurrent/next run never double-picks the same place even before this
     run finishes.
  4. Once the pipeline finishes, ``record_run_result`` goes back and fills in
     the final status + the video link, for tracking.

If web search / the LLM call / the Excel file are unavailable for any reason
(no network, no API key, openpyxl missing, etc.) this degrades gracefully to
the original deterministic rotation file (``ia_locations``), just
skipping any rotation entry whose city/state/country already appears in the
tracker -- the pipeline must never fail to pick *some* location because the
scout step had a problem.
"""

